#* En esta primer clase trabajamos creando un virtual environment. 
# Para esto, debemos situarnos en la ruta correspondiente
# y lanzar el comando para crear la carpeta de virtualenvironment. 
# py -m venv [ruta\foldername]
# foldername es el nombre del virtualenviroment, idealmente venv. 
#? Entonces el codigo seria py -m venv [ruta\venv]
#? Luego activamos el venv. Con el siguiente codigo => [ruta/venv/Scripts/activate]
# Debe de aparecer (env) y luego la ruta. Asi => (env)[ruta/venv]
#? Para desactivar el venv. Utilizamos el comando => deactivate.